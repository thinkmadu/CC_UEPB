package primeiroslide;

// tudo que for 0 ele muda pra 1 e tudo que for 1 ele muda pra 0
public class Complemento {
    public static void main(String[] args) {
        int x = -5; // nesse caso ele vai fazer -5 + 1 = -4 e vai inverter o sinal magnitude ficando +4
        System.out.println(~x);
    }
}
